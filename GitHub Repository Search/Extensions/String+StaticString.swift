import Foundation

extension String {
    /// Convenience initialiser to convert a `StaticString` into a `String`
    init(_ staticString: StaticString) {
        self = staticString.withUTF8Buffer {
            String(decoding: $0, as: UTF8.self)
        }
    }
}
